#!/usr/bin/env python3
"""
Generate C++ docstring declarations/definitions from CGAL doc JSON.

Input:
  JSON generated by src/scripts/json_from_xml.py.

Output:
  <module>_docstrings.hpp
  <module>_docstrings.cpp

The generated files are intended for the CMake binary tree, not for stale
checked-in generated source-tree directories.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path
from typing import Any


CPP_KEYWORDS = {
    "alignas", "alignof", "and", "and_eq", "asm", "auto",
    "bitand", "bitor", "bool", "break",
    "case", "catch", "char", "char8_t", "char16_t", "char32_t", "class",
    "compl", "concept", "const", "consteval", "constexpr", "constinit",
    "const_cast", "continue", "co_await", "co_return", "co_yield",
    "decltype", "default", "delete", "do", "double", "dynamic_cast",
    "else", "enum", "explicit", "export", "extern",
    "false", "float", "for", "friend",
    "goto",
    "if", "inline", "int",
    "long",
    "mutable",
    "namespace", "new", "noexcept", "not", "not_eq", "nullptr",
    "operator", "or", "or_eq",
    "private", "protected", "public",
    "register", "reinterpret_cast", "requires", "return",
    "short", "signed", "sizeof", "static", "static_assert", "static_cast",
    "struct", "switch", "synchronized",
    "template", "this", "thread_local", "throw", "true", "try", "typedef",
    "typeid", "typename",
    "union", "unsigned", "using",
    "virtual", "void", "volatile",
    "wchar_t", "while", "xor", "xor_eq",
}


OPERATOR_REPLACEMENTS = [
    ("operator()", "operator_call"),
    ("operator[]", "operator_subscript"),
    ("operator==", "operator_eq"),
    ("operator!=", "operator_ne"),
    ("operator<=", "operator_le"),
    ("operator>=", "operator_ge"),
    ("operator<<", "operator_lshift"),
    ("operator>>", "operator_rshift"),
    ("operator<", "operator_lt"),
    ("operator>", "operator_gt"),
    ("operator+=", "operator_plus_assign"),
    ("operator-=", "operator_minus_assign"),
    ("operator*=", "operator_mul_assign"),
    ("operator/=", "operator_div_assign"),
    ("operator=", "operator_assign"),
    ("operator+", "operator_plus"),
    ("operator-", "operator_minus"),
    ("operator*", "operator_mul"),
    ("operator/", "operator_div"),
    ("operator%", "operator_mod"),
    ("operator!", "operator_not"),
]


def strip_cgal_scope(name: str) -> str:
    """Remove C++ CGAL namespace qualification before identifier sanitization."""
    name = name.replace("::CGAL::", "::")
    name = name.replace("CGAL::", "")
    return name


def make_identifier(name: str) -> str:
    """Convert a C++ documented entity name into a valid C++ identifier."""
    name = strip_cgal_scope(name)

    for old, new in OPERATOR_REPLACEMENTS:
        name = name.replace(old, new)

    name = re.sub(r"<[^<>]*>", "_", name)
    ident = re.sub(r"[^A-Za-z0-9_]", "_", name)
    ident = re.sub(r"_+", "_", ident).strip("_")

    while ident.startswith("CGAL_"):
        ident = ident[len("CGAL_"):]

    if not ident:
        ident = "doc"
    if ident[0].isdigit():
        ident = f"doc_{ident}"
    if ident in CPP_KEYWORDS:
        ident = f"doc_{ident}"

    return ident


def cpp_string_literal(text: str) -> str:
    """Return a safe escaped C++ string literal."""
    return json.dumps(text, ensure_ascii=False)


def meaningful_doc(parts: list[str]) -> str:
    return "\n\n".join(p.strip() for p in parts if p and p.strip())


def member_doc(member: dict[str, Any]) -> str:
    parts: list[str] = []

    brief = member.get("brief", "").strip()
    detailed = member.get("detailed", "").strip()

    if brief:
        parts.append(brief)
    if detailed and detailed != brief:
        parts.append(detailed)

    params = []
    for param in member.get("params", []):
        pname = param.get("name", "").strip()
        pbrief = param.get("brief", "").strip() or param.get("detailed", "").strip()
        default = param.get("default", "").strip()
        if pname and (pbrief or default):
            line = f"{pname}"
            if default:
                line += f" = {default}"
            if pbrief:
                line += f": {pbrief}"
            params.append(line)

    if params:
        parts.append("Parameters:\n" + "\n".join(f"- {p}" for p in params))

    ret = member.get("return_type", "").strip()
    if ret and ret != "void":
        parts.append(f"Returns: {ret}")

    return meaningful_doc(parts)


def compound_doc(compound: dict[str, Any]) -> str:
    brief = compound.get("brief", "").strip()
    detailed = compound.get("detailed", "").strip()

    parts = []
    if brief:
        parts.append(brief)
    if detailed and detailed != brief:
        parts.append(detailed)

    return meaningful_doc(parts)


def load_json(path: Path) -> dict[str, Any]:
    data = json.loads(path.read_text(encoding="utf-8"))

    if "compounds" in data and isinstance(data["compounds"], dict):
        return data

    # Compatibility with the older reference format: top-level dict of compounds.
    if isinstance(data, dict):
        return {
            "schema_version": 0,
            "package": path.stem,
            "compounds": data,
        }

    raise SystemExit(f"ERROR: unsupported JSON schema in {path}")


def collect_entries(data: dict[str, Any]) -> list[tuple[str, str]]:
    entries: list[tuple[str, str]] = []
    seen: dict[str, int] = {}

    def add_entry(base_identifier: str, doc: str) -> None:
        if not doc.strip():
            return

        base_identifier = make_identifier(base_identifier)

        if base_identifier in seen:
            seen[base_identifier] += 1
            identifier = f"{base_identifier}_{seen[base_identifier]}"
        else:
            seen[base_identifier] = 0
            identifier = base_identifier

        entries.append((identifier, doc))

    compounds = data.get("compounds", {})

    for compound_name, compound in compounds.items():
        cdoc = compound_doc(compound)
        if cdoc:
            add_entry(f"{compound_name}__class__", cdoc)

        for member in compound.get("members", []):
            doc = member_doc(member)
            if not doc:
                continue

            qname = member.get("qualified_name", "").strip()
            if not qname:
                qname = f"{compound_name}::{member.get('name', 'unknown')}"

            add_entry(qname, doc)

    return entries


def write_hpp(library: str, module: str, package: str, entries: list[tuple[str, str]], out_path: Path) -> None:
    guard = make_identifier(f"{library}_{module}_docstrings_hpp").upper()
    module_ns = make_identifier(package)

    lines = [
        "// AUTO-GENERATED by src/scripts/cpp_docstrings_from_json.py; DO NOT EDIT.",
        f"// Library: {library}",
        f"// Module: {module}",
        f"// Package: {package}",
        "",
        f"#ifndef {guard}",
        f"#define {guard}",
        "",
        f"namespace {library} {{",
        "namespace docstrings {",
        f"namespace {module_ns} {{",
        "",
    ]

    for ident, _ in entries:
        lines.append(f"extern const char* const {ident};")

    lines += [
        "",
        f"}} // namespace {module_ns}",
        "} // namespace docstrings",
        f"}} // namespace {library}",
        "",
        f"#endif // {guard}",
        "",
    ]

    out_path.write_text("\n".join(lines), encoding="utf-8")


def write_cpp(library: str, module: str, package: str, entries: list[tuple[str, str]], out_path: Path) -> None:
    module_ns = make_identifier(package)
    hpp_name = f"cgalpy/{module}_docstrings.hpp"

    lines = [
        "// AUTO-GENERATED by src/scripts/cpp_docstrings_from_json.py; DO NOT EDIT.",
        f"// Library: {library}",
        f"// Module: {module}",
        f"// Package: {package}",
        "",
        f'#include "{hpp_name}"',
        "",
        f"namespace {library} {{",
        "namespace docstrings {",
        f"namespace {module_ns} {{",
        "",
    ]

    for ident, doc in entries:
        lines.append(f"extern constexpr const char* {ident} = {cpp_string_literal(doc)};")

    lines += [
        "",
        f"}} // namespace {module_ns}",
        "} // namespace docstrings",
        f"}} // namespace {library}",
        "",
    ]

    out_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--library", required=True)
    parser.add_argument("--module", required=True)
    parser.add_argument("--package", help="JSON package name and C++ docstring namespace; defaults to --module")
    parser.add_argument("--input-path", required=True, type=Path)
    parser.add_argument("--output-inc-path", required=True, type=Path)
    parser.add_argument("--output-lib-path", required=True, type=Path)
    args = parser.parse_args()

    package = args.package or args.module
    json_file = args.input_path / f"{package}.json"
    if not json_file.exists():
        print(f"ERROR: JSON not found: {json_file}", file=sys.stderr)
        return 1

    data = load_json(json_file)
    entries = collect_entries(data)

    args.output_inc_path.mkdir(parents=True, exist_ok=True)
    args.output_lib_path.mkdir(parents=True, exist_ok=True)

    hpp_path = args.output_inc_path / f"{args.module}_docstrings.hpp"
    cpp_path = args.output_lib_path / f"{args.module}_docstrings.cpp"

    write_hpp(args.library, args.module, package, entries, hpp_path)
    write_cpp(args.library, args.module, package, entries, cpp_path)

    print(f"json: {json_file}", file=sys.stderr)
    print(f"package: {package}", file=sys.stderr)
    print(f"entries: {len(entries)}", file=sys.stderr)
    print(f"header: {hpp_path}", file=sys.stderr)
    print(f"source: {cpp_path}", file=sys.stderr)

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
